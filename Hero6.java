package study;

public class Hero6 {
	String name;
	int hp;
	
	public Hero6() {
		this.name = "";
	}
	public Hero6(String name) {
		this.name = name;
	}
	public String getName() {
		return this.name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHp() {
		return this.hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
}
